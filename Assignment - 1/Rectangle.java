class Rectangle {
    public static void main(String[] args) {
        double length = 10;
        double breadth = 5;

        double area = length * breadth;
        double perimeter = 2 * (length + breadth);

        System.out.println("Area of Rectangle: " + area);
        System.out.println("Perimeter of Rectangle: " + perimeter);
    }
}

